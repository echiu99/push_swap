#include "libft.h"

int	ft_putstr(char *c)
{
	int	i;

	i = 0;
	if (c == NULL)
	{
		ft_putstr("(null)");
		return (6);
	}
	while (c[i] != '\0')
	{
		ft_putchar(c[i]);
		i++;
	}
	return (i);
}
